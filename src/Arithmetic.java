public class Arithmetic {
    public static void main(String[] args) {
        int a = 34;
        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);

        System.out.println(1-2+3/1%1);
    }
}
